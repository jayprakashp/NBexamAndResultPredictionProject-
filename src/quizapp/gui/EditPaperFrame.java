    private void btnEditQuestionsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQuestionsActionPerformed
      if(jcSubject.getSelectedIndex()==0){
     JOptionPane.showMessageDialog(null,"please choose a subject","select Subject",JOptionPane.ERROR_MESSAGE);
        return;
      }
      String examid=jcExamId.getSelectedItem().toString(); 
      int ans=JOptionPane.showConfirmDialog(null,"you select "+subjectName+"\t "+examid,"conformation",JOptionPane.YES_NO_OPTION);
      if(ans==JOptionPane.YES_OPTION){
          try{
              int totalQuestion=ExamDAO.getQuestionCountByExam(examid);
              ExamDTO exam=new ExamDTO(examid,subjectName,totalQuestion);
              EditQuestionsFrame questionframe=new EditQuestionsFrame(exam);
              questionframe.setVisible(true);
              this.dispose();
              
          }catch(SQLException ex){
       JOptionPane.showMessageDialog(null,"SQL Error "+ex,"select Subject",JOptionPane.ERROR_MESSAGE);
 
          }
      }

    }//GEN-LAST:event_btnEditQuestionsActionPerformed

