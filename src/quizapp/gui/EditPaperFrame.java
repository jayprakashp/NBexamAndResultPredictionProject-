    private void jcSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcSubjectActionPerformed
      if(validateInput()==false){
          JOptionPane.showMessageDialog(null,"please choose a subject","select Subject",JOptionPane.ERROR_MESSAGE);
          return;
      }
      
          try{
          ArrayList<String> list=ExamDAO.getExamIdBySubject(subjectName);
          if(list.size()==0){
     JOptionPane.showMessageDialog(null,"Sorry! No exam set for "+subjectName,"select Subject",JOptionPane.ERROR_MESSAGE);
     return;
          }
          jcExamId.removeAllItems();
          for(String s:list){
           jcExamId.addItem(s);
          }
      }catch(SQLException ex){
     JOptionPane.showMessageDialog(null,"SQL Error"+ex,"Error",JOptionPane.ERROR_MESSAGE);

      }
      
    }//GEN-LAST:event_jcSubjectActionPerformed

